export GO111MODULE=on
export GOROOT=/usr/local/go
export GOPATH=$HOME/go
export GOBIN=$GOPATH/bin
export PATH=$PATH:$GOROOT:$GOPATH:$GOBIN

go get -u google.golang.org/protobuf/cmd/protoc-gen-go
